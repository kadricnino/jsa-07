function toMiles(km) {
    return mi=km*0.62137;
};
toMiles(27);
console.log(mi);


function toPounds(kg) {
    return lb=kg*2.2046;
};
toPounds(105);
console.log(lb);


function toGallon(l) {
    return gallons=l*0.26417;
};
toGallon(26);
console.log(gallons)


function toFeet(m) {
    return ft=m*3.2808;
};
toFeet(1500);
console.log(ft);


function toKnots(kph) {
    return knots=kph/1.852;
};
toKnots(250);
console.log(knots);



